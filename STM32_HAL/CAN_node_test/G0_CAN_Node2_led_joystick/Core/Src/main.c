void HAL_FDCAN_RxFifo0Callback(FDCAN_HandleTypeDef *hfdcan, uint32_t RxFifo0ITs)

{


	if(RxFifo0ITs & FDCAN_IT_RX_FIFO0_NEW_MESSAGE)
	{
		msg_received = 1;
		HAL_FDCAN_GetRxMessage(hfdcan, FDCAN_RX_FIFO0, &RxHeader, rxdata);

		joystick = (rxdata[0]<<8) | rxdata[1];

		if(joystick >1948 && joystick<2148)
		{
			leftpwm = 0;
			rightpwm = 0;
		}
		else if(joystick < 2048)
		{
			leftpwm = (2048 - joystick)*2;
			rightpwm = 0;
		}
		else if (joystick > 2048)
		{
			leftpwm = 0;
			rightpwm = (joystick - 2048)*2;
		}

		__HAL_TIM_SET_COMPARE(&htim2, TIM_CHANNEL_1,leftpwm );
		__HAL_TIM_SET_COMPARE(&htim2, TIM_CHANNEL_2,rightpwm );
	}
}
